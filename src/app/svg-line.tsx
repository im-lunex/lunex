// Stagger config: each path gets a delay offset
const BASE_DURATION = 600
const STAGGER_DELAY = 120
const FADE_DURATION = 400

export default function SvgLineDrawBg() {
  const pathRefs = useRef<(SVGPathElement | null)[]>([])
  const svgRef = useRef<SVGSVGElement>(null)
  const hasRun = useRef(false)

  const runAnimation = useCallback(() => {
    const paths = pathRefs.current.filter(Boolean) as SVGPathElement[]
    if (paths.length === 0) return

    // Init all paths
    paths.forEach((el) => {
      const len = el.getTotalLength()
      el.style.strokeDasharray = `${len}`
      el.style.strokeDashoffset = `${len}`
      el.style.opacity = '0'
    })

    // Animate each path with staggered delay
    paths.forEach((el, i) => {
      const len = el.getTotalLength()
      const delay = 200 + i * STAGGER_DELAY
      // Longer paths get proportionally more draw time
      const duration = BASE_DURATION + Math.min(len * 0.4, 3000)

      animate(el, {
        opacity: [0, 0.85],
        duration: FADE_DURATION,
        delay,
        easing: 'easeInQuart',
      })

      animate(el, {
        strokeDashoffset: [len, 0],
        duration,
        delay,
        easing: 'easeInOutSine',
      })
    })
  }, [])

  useEffect(() => {
    if (hasRun.current) return
    hasRun.current = true
    const t = setTimeout(runAnimation, 200)
    return () => clearTimeout(t)
  }, [runAnimation])

  return (
    <div
      className="fixed inset-0 pointer-events-none"
      style={{ zIndex: 0 }}
      aria-hidden="true"
    >
      <svg
        ref={svgRef}
        viewBox="0 0 600 600"
        xmlns="http://www.w3.org/2000/svg"
        className="absolute"
        style={{
          width: '52vmin',
          height: '52vmin',
          top: '70%',
          left: '85%',
          transform: 'translate(-50%, -50%)',
          overflow: 'visible',
        }}
      >
        <g transform="translate(0,600) scale(0.1,-0.1)">
          {SVG_PATHS.map((p, i) => (
            <path
              key={i}
              ref={(el) => { pathRefs.current[i] = el }}
              d={p.d}
              fill="none"
              stroke={p.stroke ?? 'var(--foreground)'}
              strokeWidth={p.strokeWidth ?? 8}
              strokeLinecap="round"
              strokeLinejoin="round"
              style={{ willChange: 'stroke-dashoffset, opacity' }}
            />
          ))}
        </g>
      </svg>
    </div>
  )
}
